void raycast (void *mlx, void *win, void **texture)
{
	int x;

	x = 0;
	while (x < screenWidth)
	{
		double cameraX = 2 * x / (double)screenWidth - 1; //x-coordinate in camera space
		double rayDirX = dirX + planeX * cameraX;
		double rayDirY = dirY + planeY * cameraX;

		int mapX = posX;
		int mapY = posY;

		double sideDistX;
		double sideDistY;

		// double deltaDistX = (rayDirX == 0) ? 1e30 : fabs(1 / rayDirX);
		// double deltaDistY = (rayDirY == 0) ? 1e30 : fabs(1 / rayDirY);
		double deltaDistX = fabs(1 / rayDirX);
		double deltaDistY = fabs(1 / rayDirY);

		double perpWallDist;

		//what direction to step in x or y-direction (either +1 or -1)
		int stepX;
		int stepY;

		int hit = 0; //was there a wall hit?
		int side; //was a NS or a EW wall hit?
		//calculate step and initial sideDist
		if(rayDirX < 0)
		{
			stepX = -1;
			sideDistX = (posX - mapX) * deltaDistX;
		}
		else
		{
			stepX = 1;
			sideDistX = (mapX + 1.0 - posX) * deltaDistX;
		}
		if(rayDirY < 0)
		{
			stepY = -1;
			sideDistY = (posY - mapY) * deltaDistY;
		}
		else
		{
			stepY = 1;
			sideDistY = (mapY + 1.0 - posY) * deltaDistY;
		}

		while(hit == 0)
		{
			//jump to next map square, either in x-direction, or in y-direction
			if(sideDistX < sideDistY)
			{
				sideDistX += deltaDistX;
				mapX += stepX;
				side = 0;
			}
			else
			{
				sideDistY += deltaDistY;
				mapY += stepY;
				side = 1;
			}
			//Check if ray has hit a wall
			if(worldMap[mapX][mapY] > 0) hit = 1;
		}
		if(side == 0) perpWallDist = (sideDistX - deltaDistX);
		else          perpWallDist = (sideDistY - deltaDistY);

		//Calculate height of line to draw on screen
		int lineHeight = (int)(screenHeight / perpWallDist);


		//FIXME:diff
		int pitch = 100;



		//calculate lowest and highest pixel to fill in current stripe
		int drawStart = -lineHeight / 2 + screenHeight / 2 + pitch;
		if(drawStart < 0) drawStart = 0;
		int drawEnd = lineHeight / 2 + screenHeight / 2 + pitch;
		if(drawEnd >= screenHeight) drawEnd = screenHeight - 1;

		int texNum = worldMap[mapX][mapY] - 1;

		double wallX;
		if (side == 0)
			wallX = posY + perpWallDist * rayDirY;
		else
			wallX = posX + perpWallDist * rayDirX;

		int texX = (int) (wallX * texWidth);
		if (side == 0 && rayDirX > 0)
			texX = texWidth - texX - 1;
		if (side == 1 && rayDirY < 0)
			texX = texWidth - texX - 1;
		
		double step = 1.0 * texHeight / lineHeight;
		double texPos = (drawStart - pitch - screenHeight / 2 + lineHeight / 2) * step;
		

		int y = drawStart;
		// while (y < drawEnd)
		// {
		// 	int texY = (int)texPos & (texHeight - 1);
		// 	texPos += step;
		// 	__int32_t color = texture[texNum][texHeight * texY + texX];
		// 	++y;
		// }
		++x;
	}
}

